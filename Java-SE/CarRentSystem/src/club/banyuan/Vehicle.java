package club.banyuan;

public class Vehicle {
    private String type;
    private int appearenceYear;
    private String factory;
    private String id;
    private String lastMaintenanceTime;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getAppearenceYear() {
        return appearenceYear;
    }

    public void setAppearenceYear(int appearenceYear) {
        this.appearenceYear = appearenceYear;
    }

    public String getFactory() {
        return factory;
    }

    public void setFactory(String factory) {
        this.factory = factory;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLastMaintenanceTime() {
        return lastMaintenanceTime;
    }

    public void setLastMaintenanceTime(String lastMaintenanceTime) {
        this.lastMaintenanceTime = lastMaintenanceTime;
    }
}
